package hu.opau.voltvault.models;

public class ProductReview {

    public ProductReview() {}


    private String name;
    private String product;
    private String text;
    private Float value;

    public String getName() {
        return name;
    }

    public String getProduct() {
        return product;
    }

    public String getText() {
        return text;
    }

    public Float getValue() {
        return value;
    }
}
